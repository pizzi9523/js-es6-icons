document.getElementById("icons").addEventListener("change", function () {
    document.querySelector(".cards").innerHTML = ""
    const selectedIcons = icons.filter(icon => icon.type === this.value)
    if (this.value == "all") {
        generateIcons(icons)
    }
    generateIcons(selectedIcons)
})


function generateIcons(icons) {
    icons.forEach(icon => {

        const card =
            `
        <div class="col-2">
            <div class="my_card text-center">
                <i class="${icon.family} ${icon.prefix}${icon.name} ${icon.type} fa-2x"></i>
                <div class="icon_name">
                    <div>${icon.name.toUpperCase()}</div>
                </div>
            </div>
        </div>`
        document.querySelector('.row').insertAdjacentHTML('beforeend', card)
    });

}

